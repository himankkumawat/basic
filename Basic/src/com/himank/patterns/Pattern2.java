package com.himank.patterns;

public class Pattern2 {

    public static void main(String[] args) {
        int i, j;
        for (i = 01; i <= 5; i++) {
            for (j = 0; j < 5; j++) {
                System.out.print(i);
            }
            System.out.println();
        }
    }

//            Output:
//            11111
//            22222
//            33333
//            44444
//            55555

}
